#define CS8409_CS42L42_HP_VOL_REAL_MAX		(0)
#define CS8409_CS42L42_AMIC_VOL_REAL_MIN	(-97)
#define CS8409_CS42L42_AMIC_VOL_REAL_MAX	(12)

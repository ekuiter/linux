#define CS8409_CS42L42_REG_HS_VOL_CHB		(0x2303)
#define CS8409_CS42L42_REG_HS_VOL_MASK		(0x003F)
#define CS8409_CS42L42_REG_AMIC_VOL		(0x1D03)
#define CS8409_CS42L42_REG_AMIC_VOL_MASK	(0x00FF)
